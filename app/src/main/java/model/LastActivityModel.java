package model;

public class LastActivityModel {

    String userNames_;
    String lastActivity;
    String deviceParameter;


    public String getUserNames_() {
        return userNames_;
    }

    public void setUserNames_(String userNames_) {
        this.userNames_ = userNames_;
    }

    public String getLastActivity() {
        return lastActivity;
    }

    public void setLastActivity(String lastActivity) {
        this.lastActivity = lastActivity;
    }

    public String getDeviceParameter() {
        return deviceParameter;
    }

    public void setDeviceParameter(String deviceParameter) {
        this.deviceParameter = deviceParameter;
    }
}
